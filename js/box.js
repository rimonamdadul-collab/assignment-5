document.getElementById('ShopEase').addEventListener('click',
    function(event){
        const ShopEaseDiv=document.getElementById('ShopEase-div')
        const p=document.createElement('p')
        const time = new Date().toLocaleTimeString()
        p.innerText=`You have Complete The Task Add Dark Mode at ${time}`;
        ShopEaseDiv.appendChild(p)
        p.classList.add('bg-[#F4F7FF] rounded-lg text-sm')

        const sum=document.getElementById('number-sum').innerText;
        let convertedSum=parseInt(sum)
        convertedSum+1
        const diff=document.getElementById('number-diff').innerText;
        let convertedDiff=parseInt(diff)
        convertedDiff-1
        

        
    }
)
